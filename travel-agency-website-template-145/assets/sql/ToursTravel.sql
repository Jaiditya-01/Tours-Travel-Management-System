CREATE DATABASE TourTravelDB;
USE TourTravelDB;
-- DROP DATABASE TourTravelDB;

-- Customer Table

CREATE TABLE Customer (
    Customer_ID INT AUTO_INCREMENT PRIMARY KEY,
    Name VARCHAR(100) NOT NULL,
    Email VARCHAR(100) UNIQUE NOT NULL, 
    Phone VARCHAR(20) UNIQUE NOT NULL,
    Address TEXT,
    Date_Of_Birth DATE,
    Nationality VARCHAR(50),
    User_Type ENUM('Regular', 'Admin') NOT NULL,
    Password VARCHAR(20) NOT NULL
);

-- Tour Package Table
CREATE TABLE TourPackage (
    TourPackage_ID INT AUTO_INCREMENT PRIMARY KEY,
    Name VARCHAR(100) NOT NULL,
    Price DECIMAL(10,2) NOT NULL,
    Duration INT NOT NULL,
    Max_People INT,
    Difficulty_Level ENUM('Easy', 'Moderate', 'Hard'),
    Description TEXT,
    Image_URL VARCHAR(255)
);

-- Booking Table
CREATE TABLE Booking (
    Booking_ID INT AUTO_INCREMENT PRIMARY KEY,
    Customer_ID INT,
    TourPackage_ID INT,
    Booking_Date DATE NOT NULL,
    Status ENUM('Pending', 'Confirmed', 'Cancelled') NOT NULL,
    NumberOfPeople INT NOT NULL,
    Total_Cost DECIMAL(10,2) NOT NULL,
    FOREIGN KEY (Customer_ID) REFERENCES Customer(Customer_ID) ON DELETE CASCADE,
    FOREIGN KEY (TourPackage_ID) REFERENCES TourPackage(TourPackage_ID) ON DELETE CASCADE
);

-- Payment Table
CREATE TABLE Payment (
    Payment_ID INT AUTO_INCREMENT PRIMARY KEY,
    Booking_ID INT,
    Amount DECIMAL(10,2) NOT NULL,
    Currency VARCHAR(10) NOT NULL,
    Payment_Date DATE NOT NULL,
    Status ENUM('Pending', 'Completed', 'Failed') NOT NULL,
    Payment_Method ENUM('Credit Card', 'Debit Card', 'UPI', 'Bank Transfer') NOT NULL,
    Transaction_ID VARCHAR(50) UNIQUE,
    FOREIGN KEY (Booking_ID) REFERENCES Booking(Booking_ID) ON DELETE CASCADE
);

-- Review Table
CREATE TABLE Review (
    Review_ID INT AUTO_INCREMENT PRIMARY KEY,
    Customer_ID INT,
    TourPackage_ID INT,
    Rating INT CHECK(Rating BETWEEN 1 AND 5),
    Review_Title VARCHAR(255),
    Comment TEXT,
    Review_Date DATE NOT NULL,
    Image_URL VARCHAR(255),
    FOREIGN KEY (Customer_ID) REFERENCES Customer(Customer_ID) ON DELETE CASCADE,
    FOREIGN KEY (TourPackage_ID) REFERENCES TourPackage(TourPackage_ID) ON DELETE CASCADE
);

-- Destination Table
CREATE TABLE Destination (
    Destination_ID INT AUTO_INCREMENT PRIMARY KEY,
    Name VARCHAR(100) NOT NULL,
    Country VARCHAR(100) NOT NULL,
    Region VARCHAR(100),
    Description TEXT,
    Latitude DECIMAL(9,6),
    Longitude DECIMAL(9,6)
);

-- Tour Guide Table
CREATE TABLE TourGuide (
    Guide_ID INT AUTO_INCREMENT PRIMARY KEY,
    Name VARCHAR(100) NOT NULL,
    Languages TEXT NOT NULL,
    Experience INT NOT NULL,
    Contact_Number VARCHAR(20) UNIQUE,
    Availability_Status ENUM('Available', 'Unavailable') NOT NULL
);

-- Tour Package Pricing Table
CREATE TABLE TourPackagePricing (
    Pricing_ID INT AUTO_INCREMENT PRIMARY KEY,
    TourPackage_ID INT,
    Season VARCHAR(50) NOT NULL,
    Price DECIMAL(10,2) NOT NULL,
    Start_Date DATE NOT NULL,
    End_Date DATE NOT NULL,
    Discounted_Price DECIMAL(10,2),
    FOREIGN KEY (TourPackage_ID) REFERENCES TourPackage(TourPackage_ID) ON DELETE CASCADE
);

-- Tour Category Table
CREATE TABLE TourCategory (
    Category_ID INT AUTO_INCREMENT PRIMARY KEY,
    Name VARCHAR(100) NOT NULL,
    Description TEXT,
    Icon_URL VARCHAR(255)
);

-- Activity Type Table
CREATE TABLE ActivityType (
    ActivityType_ID INT AUTO_INCREMENT PRIMARY KEY,
    Name VARCHAR(100) NOT NULL,
    Description TEXT
);

-- Tour Package Schedule Table
CREATE TABLE TourPackageSchedule (
    Schedule_ID INT AUTO_INCREMENT PRIMARY KEY,
    TourPackage_ID INT,
    Destination_ID INT,
    Day INT NOT NULL,
    Activity VARCHAR(255) NOT NULL,
    Scheduled_Date DATE NOT NULL,
    Start_Time TIME,
    End_Time TIME,
    FOREIGN KEY (TourPackage_ID) REFERENCES TourPackage(TourPackage_ID) ON DELETE CASCADE,
    FOREIGN KEY (Destination_ID) REFERENCES Destination(Destination_ID) ON DELETE CASCADE
);

-- FAQ Table
CREATE TABLE FAQ (
    FAQ_ID INT AUTO_INCREMENT PRIMARY KEY,
    Question TEXT NOT NULL,
    Answer TEXT NOT NULL
);

-- Discount Table
CREATE TABLE Discount (
    Discount_ID INT AUTO_INCREMENT PRIMARY KEY,
    TourPackage_ID INT,
    Discount_Code VARCHAR(50) UNIQUE NOT NULL,
    Discount_Percentage DECIMAL(5,2) NOT NULL,
    Start_Date DATE NOT NULL,
    End_Date DATE NOT NULL,
    FOREIGN KEY (TourPackage_ID) REFERENCES TourPackage(TourPackage_ID) ON DELETE CASCADE
);

-- Tour Package Amenities Table
CREATE TABLE TourPackageAmenities (
    Amenity_ID INT AUTO_INCREMENT PRIMARY KEY,
    TourPackage_ID INT,
    Amenity_Type VARCHAR(100) NOT NULL,
    Amenity_Name VARCHAR(100) NOT NULL,
    Description TEXT,
    Available BOOLEAN NOT NULL DEFAULT TRUE,
    Additional_Cost DECIMAL(10,2),
    Terms_And_Conditions TEXT,
    FOREIGN KEY (TourPackage_ID) REFERENCES TourPackage(TourPackage_ID) ON DELETE CASCADE
);

-- Loyalty Program Table
CREATE TABLE LoyaltyProgram (
    LoyaltyProgram_ID INT AUTO_INCREMENT PRIMARY KEY,
    Customer_ID INT,
    Points_Accumulated INT DEFAULT 0,
    Points_Redeemed INT DEFAULT 0,
    Membership_Level ENUM('Bronze', 'Silver', 'Gold', 'Platinum') NOT NULL,
    FOREIGN KEY (Customer_ID) REFERENCES Customer(Customer_ID) ON DELETE CASCADE
);

